                            <h1>ABOUT</h1>
                            <div class="sep10"></div>
V2EX is a community of start-ups, designers, developers and creative people.
<br />
<br />V2EX 是创意工作者们的社区。这里目前汇聚了超过 50000 名主要来自互联网行业、游戏行业和媒体行业的创意工作者。V2EX 希望能够成为创意工作者们的生活和事业的一部分。
<br />
<br />希望大家能够多多分享自己正在做的有趣事物、交流想法，在这里找到朋友甚至新的机会。并且，最重要的是，在这一切的过程中，保持对他人的友善。
<br />
<br />为了保持这里的良好氛围，V2EX 有自己的明确规则：
<br />
<br />• 这里绝对不讨论任何有关盗版软件、音乐、电影如何获得的问题
<br />• 这里绝对不会全文转载任何文章，而只会以链接方式分享
<br />• 这里感激和崇尚美的事物
<br />• 这里尊重原创
<br />• 这里反对中文互联网上的无信息量习惯如“顶”，“沙发”，“前排”，“留名”，“路过”
<br />

<br />V2EX is powered by Cloud Community Software - Project Babel 3
<br />
<br />为了获得访问 V2EX 的最佳体验，我们推荐使用 <a href="http://chrome.google.com/" target="_blank">Google Chrome</a> 或 <a href="http://www.getfirefox.com/" target="_blank">Mozilla Firefox</a> 浏览器。
<br />
<br /><a href="https://workspace.v2ex.com/" target="_blank">V2EX Workspace</a> 是我们于 2011 年 10 月 10 日推出的全新产品——一个适合小团队和个人使用的信息整理和分享工具。

<br />
<br /><!--<h2 style="display: inline;">A Bit of History</h2>
<br />
<br />2005 年 6 月 8 日，一个不算太热的夏日下午，V2EX 这四个字母从我的脑子中划过，紧随而来的就是我关于这 4 个字母而想出的各种延伸意义。而我的运气真的很好，我注册到了 V2EX.com 这个域名。
<br />
<br />用这个域名来做什么，一开始我并没有太清楚的想法。那个时候 Web 2.0 的概念正在兴起，而 Ajax 是非常热门的技术。于是另外一句话抓住了我的心，成为了当时 V2EX 的副标题：
<br />
<br />software for internet
<br />
<br />那个时候我有一台托管在上海电信的 Dell 1850 服务器，并且在上面安装了 ColdFusion 的 runtime，于是 2005 年 6 月时的 V2EX，就是一个黑色背景的 .cfm 页面，上面用白色的 Arial Black 字体，缩小了字符间距，写上了 V2EX，及 software for internet。
<br />
<br />2005 年底的时候，我用 PHP + MySQL 完成了一个社区软件，代号 Project Babel。在经过了几个月的准备及数次重新设计之后，我在 2006 年 3 月以社区形式让 V2EX 上线了。在那个时候，V2EX 的含义也同时被定义为：
<br />
<br />way to explore
<br />
<br />及
<br />
<br />way too extreme
<br />
<br />我想，前者是一种方式，而后者，是一种态度。
<br />
<br />后来有人告诉我，v 和 w 作为单词首字母时，发音还是有细微差异的。不过，对于大部分人来说，这样的差异实在是够小。
<br />
<br />在 2006 年 4 月时，Project Babel R220 在 Google Code 发布。R220 表示那是 Subversion 中第 220 次 commit 的版本。
<br />
<br />整体而言，2006 年是十分快乐的一年，那一年我 21 岁。我可以将自己的大部分时光都用来做自己最感兴趣的事情。我并没有把自己当成一个严肃产品的产品经理，于是我在自己的软件中添加了任何我觉得有趣的东西：dictd 查询，基于 PHP Lucene 的开源软件文档搜索，RSS 聚合……
<br />
<br />2006 年底，我以一个小众网站站长的身份受到了三联生活周刊的采访。然后在 2007 年初杂志出街的第二天，V2EX 在上海的服务器被拔了网线。
<br />
<br />我不知道发生了什么，除了莫名惊诧，我只能设法尽快恢复网站及我的开发进度。那段时间很多朋友帮助了我，我甚至收到了来自用户的捐款（最大的一笔 300 美元），以帮助我将服务器架设到美国。而经过了整个 2006 年，我起码明白了自己为什么要做这一切──那只是一个简单的目的──写出让自己满意的足够好用的 software for internet，并且把成果分享出去。
<br />
<br />2007 年 9 月，V2EX.com 这个域名莫名其妙的成为了一个关键字，如果你在 Google 中搜索这个域名，于是几分钟之内你就不能访问 Google 了。而美国服务器的 IP 地址，也遭遇了封锁。
<br />
<br />那段时间，作为一个 geek，作为一个每天有超过 10 个钟头在面对电脑的宅男，我研究了很多技术，那些技术让我可以继续访问 V2EX 及 Wikipedia。
<br />
<br />2008 年 2 月底，一切的感觉都变得很奇怪，V2EX 本身的活跃度也每况愈下。于是我想，或许该到了 pause 的时候了。于是在 2 月底，V2EX 下线，基于 LAMP 技术的 Project Babel 最后的版本号是 0.7-devel。
<br />
<br />你或许会发现我在描述上面的一系列事件时，似乎省略了许多的细节。是的，一来已经发生的事情已太过久远，而回忆过去不如多想想未来。
<br />
<br />以上是历史。
<br />
<br />而你现在看到的，是 2010 年全新上线的 V2EX，一个基于 Google App Engine 技术搭建的社区。
<br />
<br />选择这个技术的主要原因是数据安全。把一个网站的核心数据放到云计算平台中，实在是最让人安心的选择。不用担心硬盘或是 CPU 烧掉。如此一来，便可以将全部精力用于新功能的开发和加强。Cloud Computing 同时也意味着很多全新的开发方式，而对于我这样的技术爱好者来说，学习新东西实在是无比愉快。
<br />
<br />V2EX 的含义没变，依然是 way to explore 和 way too extreme，我希望这里能够成为对于很多人而言，最好的探索和分享之地。
